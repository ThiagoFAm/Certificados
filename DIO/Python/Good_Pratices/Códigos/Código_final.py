n1 = int(20)
n2 = int(30)

def Sum_sub(n1, n2):
    """  Soma e subtração
    :n1: = Valor predefinido pelo código
    :n2: = Valor predefinido pelo código
    :sum: = Soma de :n1: e :n2:
    :sub: = Subtração de :n1: e :n2:
    :return: sum e sub
    """
    
    sum = n1 + n2
    sub = n1 - n2
    
    return sum, sub
